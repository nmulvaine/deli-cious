package com.pluralsight.capstone2.utilities;

public class MenuItem
{
    private String bread, breadSize, meat, meatAmount, cheese, cheeseAmount, veggie, sauce, drink, drinkSize, chip;

    public String getBread()
    {
        return bread;
    }

    public void setBread(String bread)
    {
        this.bread = bread;
    }

    public String getBreadSize()
    {
        return breadSize;
    }

    public void setBreadSize(String breadSize)
    {
        this.breadSize = breadSize;
    }

    public String getCheese()
    {
        return cheese;
    }

    public void setCheese(String cheese)
    {
        this.cheese = cheese;
    }

    public String getCheeseAmount()
    {
        return cheeseAmount;
    }

    public void setCheeseAmount(String cheeseAmount)
    {
        this.cheeseAmount = cheeseAmount;
    }

    public String getChip()
    {
        return chip;
    }

    public void setChip(String chip)
    {
        this.chip = chip;
    }

    public String getDrink()
    {
        return drink;
    }

    public void setDrink(String drink)
    {
        this.drink = drink;
    }

    public String getDrinkSize()
    {
        return drinkSize;
    }

    public void setDrinkSize(String drinkSize)
    {
        this.drinkSize = drinkSize;
    }

    public String getMeat()
    {
        return meat;
    }

    public void setMeat(String meat)
    {
        this.meat = meat;
    }

    public String getMeatAmount()
    {
        return meatAmount;
    }

    public void setMeatAmount(String meatAmount)
    {
        this.meatAmount = meatAmount;
    }

    public String getSauce()
    {
        return sauce;
    }

    public void setSauce(String sauce)
    {
        this.sauce = sauce;
    }

    public String getVeggie()
    {
        return veggie;
    }

    public void setVeggie(String veggie)
    {
        this.veggie = veggie;
    }
}